cat("Panel A (id_9 Dranove et al.):\n")
print(st9  %>% select(spec, beta, G, mvpf), row.names = FALSE)
cat("\nPanel B (id_420 Bailey & Goodman-Bacon):\n")
print(st420 %>% select(spec, beta, G, mvpf), row.names = FALSE)

cat("\nSaved:\n")
cat(" ", file.path(out_dir, "figure_4_8_mvpf_stress_test_panelA.pdf"), "\n")
cat(" ", file.path(out_dir, "figure_4_8_mvpf_stress_test_panelA.png"), "\n")
cat(" ", file.path(out_dir, "figure_4_8_mvpf_stress_test_panelB.pdf"), "\n")
